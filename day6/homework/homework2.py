# თუ 10000-ზე მეტია დაპრინტეთ გოაში სწავლობდი?
# თუ 1000-დან 10000_მდეა დაპრინტეთ "you mid"
# თუ 1000_ზე დაბალია დაპრინტეთ "შემოსულიყავი გოაში მატრიცელო"

user_salary = int(input("Enter User Salary: "))
if user_salary > 10000:
    print("გოაში სწავლობდი?")
elif user_salary >= 1000 and user_salary <= 10000:
    print("you mid")
else:
    print("შემოსულიყავი გოაში, მატრიცელო")